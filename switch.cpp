# include <bits/stdc++.h>
using namespace std;

int main()
{
    int x_pos = 0,y_pos = 0;
    char move;

    switch(move){
        case 'l' : x_pos --; 
                    break;
        case 'r' : y_pos ++;
                    break;
        default:
            cout <<"Enter a valid input";   
    }
        
}